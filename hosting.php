Параметры виртуального хостинга:
	Главные:
		 - Uptime (надежность)  - сколько сайт будет находится онлайн наприме в 24 часа (сервер может перезагружаться и т.д.)
		 - Быстродействие
		 - Выдерживаемая нагрзука   -  сколько посетителей выдерживает сервер
		 - Время отклика          -  зависит от месторасположения сервера (если из америки например время отклика немного увеличится).
	Второстепенные
		 - Год основания
         - Технические параметры
         - Цена

!!!!!   Про главные параметры в хостингах не пишут. Можно только сравнивать, покупая разные хостинги.
		Это проделано на https://hosting-ninja.ru/rating/




  -------------------------------------- 7-8) Покупаем виртуальный хостинг/Связываем домен и хостинг ---------------------------------


На примере  https://mchost.ru/
 Рагистрируемся и выбираем тариф.

Связываем домен и хостинг.
Переходим к регистратору домена reg.ru. И в нужном домене указываем DNS-серверы от хостинга который мы купили (эти домены могут быть указаны в письме от хостинга).
И в течение 24 часа будет связка : этот домен принадлежит этому хостингу.


